export const catalogs: Record<SupportedLocale, TranslationCatalog> = {
  en: english,
  'zh-CN': zhCN,
  ja: japanese,
  ko: korean,
  es: spanish,
  fr: french,
}

let currentLocale: SupportedLocale = 'en'

export function normalizeLocale(language?: string): SupportedLocale {
  const normalized = language?.trim().toLowerCase().replace(/_/g, '-')
  if (!normalized) return 'en'
  if (normalized === 'zh' || normalized.startsWith('zh-')) return 'zh-CN'
  if (normalized === 'ja' || normalized.startsWith('ja-')) return 'ja'
  if (normalized === 'ko' || normalized.startsWith('ko-')) return 'ko'
  if (normalized === 'es' || normalized.startsWith('es-')) return 'es'
  if (normalized === 'fr' || normalized.startsWith('fr-')) return 'fr'
  return 'en'
}

/** Uses Obsidian's getLanguage when a compatible host supplies it, then the browser locale. */
export function resolveLocale(
  getLanguage?: (() => string) | undefined,
  navigatorLanguage?: string
): SupportedLocale {
  try {
    const hostLanguage = getLanguage?.()
    if (hostLanguage) return normalizeLocale(hostLanguage)
  } catch {
    // Older Obsidian versions do not expose getLanguage; use the browser fallback.
  }
  return normalizeLocale(navigatorLanguage)
}

export function setLocale(locale: string): SupportedLocale {
  currentLocale = normalizeLocale(locale)
  return currentLocale
}

export function getLocale(): SupportedLocale {
  return currentLocale
}

export function translate(locale: string, key: string, values: TranslationValues = {}): string {
  const catalog = catalogs[normalizeLocale(locale)]
  const template = catalog[key as TranslationKey] ?? english[key as TranslationKey] ?? key
  return template.replace(/\{([\w-]+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`))
}

export function t(key: TranslationKey, values?: TranslationValues): string {
  return translate(currentLocale, key, values)
}

export function formatUsd(amount: number, locale = currentLocale): string {
  return new Intl.NumberFormat(locale, { style: 'currency', currency: 'USD' }).format(amount)
}
